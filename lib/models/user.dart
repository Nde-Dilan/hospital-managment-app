class User {
  String name;
  String age;
  String gender;
  String email;
  String password;
  String phoneNumber;
  String location;
  String image;

  User({
    required this.age,
    required this.gender,
    required this.email,
    required this.phoneNumber,
    required this.password,
    required this.name,
    required this.location,
    required this.image,
  });

}

